function drawChessboard() {
  
  var board = "";

  var grid = 8;

  for (var a = 0; a < grid; a++) {
    for (var b = 0; b < grid; b++) {
      if ((a + b) % 2 === 0) {
        board += " ";
      } else {
        board += "#";
      }
    }
    board += "\n";
  }
} 



