// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if (num1 < num2) {
    return num1;
  } else {
    return num2;
  }

}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(posNum) {
  if (posNum === 0) {
    return true;
  } else if (posNum === 1) {
    return false;
  } else if(posNum < 0) {
    return isEven(-posNum);
  }else {
    return isEven(posNum - 2);
  } 
  


}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  var count = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === char) {
      count += 1;
    }
  } 
return count;


}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var count = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === "B") {
      count += 1;
    }
  }
return count;
}

